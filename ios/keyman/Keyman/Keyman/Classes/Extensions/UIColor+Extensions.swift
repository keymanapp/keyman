import Foundation
import UIKit

extension UIColor {
  static var  keymanOrange: UIColor {
    return UIColor(red: 244/255, green: 113/255, blue: 32/255, alpha: 1.0)
  }
  
  static var  keymanRed: UIColor {
    return UIColor(red: 186/255, green: 32/255, blue: 52/255, alpha: 1.0)
  }
  
  static var  keymanBlue: UIColor {
    return UIColor(red: 105/255, green: 183/255, blue: 211/255, alpha: 1.0)
  }
}
